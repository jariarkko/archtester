//
// Map a DNS name to an address
//

static void
archtesterd_getdestinationaddress(const char* destination,
				  struct sockaddr_in* address) {
  
  struct addrinfo hints, *res;
  struct sockaddr_in *addr;
  int rcode;
  
  archtesterd_assert(destination != 0);
  archtesterd_assert(address != 0);
  
  memset(&hints,0,sizeof(hints));
  hints.ai_family = AF_INET;
  hints.ai_socktype = SOCK_STREAM;
  hints.ai_flags = hints.ai_flags | AI_CANONNAME;
  
  if ((rcode = getaddrinfo(destination, NULL, &hints, &res)) != 0) {
    fatalf("cannot resolve address %s: %s", destination, gai_strerror (rcode));
    exit(1);
  }
  
  *address = *(struct sockaddr_in*)res->ai_addr;
}

//
// Convert an IPv4 address to string
//

static const char*
archtesterd_addrtostring(struct in_addr* addr) {
  archtesterd_assert(addr != 0);
  return(inet_ntoa(*addr));
}

static const char*
archtesterd_tlsver_versiontostring(unsigned short version) {
  static char buf[40];
  
  if (version == 0) {
      
    sprintf(buf,"unknown");

  } else if (!ARCHTESTERD_TLSVER_TLS_HELLO_TLS_VERSION_IS_VALID(version)) {

    sprintf(buf,"unknown");

  } else if (ARCHTESTERD_TLSVER_TLS_HELLO_TLS_VERSION_13_IS_DRAFT(version)) {
    
    if (draft) {
      sprintf(buf,"1.3 draft %u", ARCHTESTERD_TLSVER_TLS_HELLO_TLS_VERSION_13_DRAFT_VER(version));
    } else {
      sprintf(buf,"1.3");
    }
    
  } else {
    
    unsigned char majorver = ARCHTESTERD_TLSVER_TLS_HELLO_TLS_VERSION_MAJOR(version);
    unsigned char minorver = ARCHTESTERD_TLSVER_TLS_HELLO_TLS_VERSION_MINOR(version);
    sprintf(buf,"%u.%u", majorver, minorver);
    
  }
  
  return(buf);
}
 
static unsigned short
archtesterd_tlsver_getversion() {
  if (version_supported != 0) return(version_supported);
  if (version_hello != 0) return(version_hello);
  if (version_record != 0) return(version_record);
  return(0);
}
 
static int
archtesterd_connect_withtimeout(int sock,
	                        struct sockaddr* address,
                          	size_t asize,
                                struct timeval* timeout) {

  fd_set writes;

  fcntl(sock, F_SETFL, O_NONBLOCK);
  debugf("doing non-blocking connect");
  connect(sock, address, asize);

  FD_ZERO(&writes);
  FD_SET(sock, &writes);

  debugf("doing a select to wait for the connect to complete");
  
  if (select(sock + 1, 0, &writes, 0, timeout) == 1) {
    int so_error;
    socklen_t len = sizeof so_error;
	
    getsockopt(sock, SOL_SOCKET, SO_ERROR, &so_error, &len);
    if (so_error == 0) {
      debugf("socket is open");
      return(0);
    }
  }
  
  debugf("either timeout or other error");
  return(-1);
}

//
// Run the actual probing
//

static const char*
archtesterd_tlsver_runtest(const char* destination) {
  
  int sock;
  struct timeval timeout;
  struct sockaddr_in server;
  unsigned char sentMessage[ARCHTESTERD_TLSVER_MAXMSGSIZE];
  unsigned int sentMessageSize;
  unsigned char receivedMessage[ARCHTESTERD_TLSVER_MAXMSGSIZE];
  int receivedMessageSize;
  unsigned int position = 0;
  unsigned int currentLength = 0;
  
  //
  // Create socket
  //
  
  sock = socket(AF_INET , SOCK_STREAM , 0);
  if (sock == -1) {
    fatalf("could not create socket");
  }
  
  archtesterd_getdestinationaddress(destination,&server);
  server.sin_port = htons(port);
  debugf("connecting to %s (%s):%u",
	 destination,
	 archtesterd_addrtostring(&server.sin_addr),
	 port);

  //
  // Connect to remote server
  //
  
  timeout.tv_sec = ARCHTESTERD_TLSVER_MAXWAIT_CONNECT_SECS;
  timeout.tv_usec = 0;
  if (archtesterd_connect_withtimeout(sock , (struct sockaddr *)&server , sizeof(server), &timeout) < 0) {
    fatalf("connect failed");
  }
  
  //
  // Communicate with the server
  //
  
  archtesterd_tlsver_makeclienthello(sentMessage,sizeof(sentMessage),&sentMessageSize);
  archtesterd_showbytes("sending",sentMessage,sentMessageSize);
  if (send(sock,
	   (const char*)sentMessage,
	   sentMessageSize,
	   0) < 0) {
    fatalf("send failed");
  }

  do {

    //
    // Receive a reply from the server
    //

    struct timeval now;
    fd_set reads;
    int selres;
    unsigned long long diff;
    
    if (interrupt) exit(1);
    
    archtesterd_tlsver_getcurrenttime(&now);
    diff = archtesterd_timediffinusecs(&now,&startTime);
    if (diff >= ARCHTESTERD_TLSVER_MAXWAIT_USECS)
      fatalf("timed out");
    
    timeout.tv_sec = 0;
    timeout.tv_usec = 50 * 1000;
    
    FD_ZERO(&reads);
    FD_SET(sock,&reads);
    debugf("going into select for %lu s %lu us", timeout.tv_sec, timeout.tv_usec);
    selres = select(1, &reads, 0, 0, &timeout);
    
    debugf("going to read more from socket (already %u bytes)", currentLength);
    if ((receivedMessageSize = recv(sock,
	                            (void*)&receivedMessage[currentLength],
				    ARCHTESTERD_TLSVER_MAXMSGSIZE - currentLength,
				    MSG_DONTWAIT)) < 0) {
      if (errno == EAGAIN || errno == EWOULDBLOCK) continue;
      else fatalf("recv failed");
    }

    currentLength += receivedMessageSize;
    archtesterd_showbytes("received",receivedMessage,currentLength);
    
  } while (archtesterd_tlsver_parseservermessages(receivedMessage,currentLength,&position) ==
	   archtesterd_tlsver_result_waitformore);
  
  close(sock);

  //
  // Return with knowledge of what version this was
  //
  
  return(archtesterd_tlsver_versiontostring(archtesterd_tlsver_getversion()));
}

//
// Report result
//

static void
archtesterd_tlsver_report(const char* result) {
  if (result == 0) printf("unknown\n");
  else printf("%s\n", result);
}

//
// Main program ------------------------------------------------
//

int
main(int argc,
     char** argv) {

  const char* result;
  
  //
  // Process arguments
  //
  
  argc--; argv++;
  while (argc > 0) {
    
    if (strcmp(argv[0],"-version") == 0) {
      
      printf("version 0.2\n");
      exit(0);
      
    } else if (strcmp(argv[0],"-quiet") == 0) {
      
      quiet = 1;
      
    } else if (strcmp(argv[0],"-not-quiet") == 0) {
      
      quiet = 0;
      
    } else if (strcmp(argv[0],"-debug") == 0) {
      
      debug = 1;
      
    } else if (strcmp(argv[0],"-no-debug") == 0) {
      
      debug = 0;
      
    } else if (strcmp(argv[0],"-compact") == 0) {
      
      compact = 1;
      
    } else if (strcmp(argv[0],"-no-compact") == 0) {
      
      compact = 0;
      
    } else if (strcmp(argv[0],"-draft") == 0) {
      
      draft = 1;
      
    } else if (strcmp(argv[0],"-rfc") == 0) {
      
      draft = 0;
      
    } else if (strcmp(argv[0],"-port") == 0 && argc > 1 && isdigit(argv[1][0])) {

      port = atoi(argv[1]);
      argc--; argv++;
      
    } else if (argv[0][0] == '-') {
      
      fatalf("unrecognised option %s", argv[0]);
      
    } else if (argc > 1) {
      
      fatalf("too many arguments");
      
    } else {
      
      testDestination = argv[0];
      
    }
    
    argc--; argv++;
    
  }
  
  signal(SIGINT, archtesterd_tlsver_interrupt);
  
  archtesterd_tlsver_getcurrenttime(&startTime);
  
  result = archtesterd_tlsver_runtest(testDestination);
  archtesterd_tlsver_report(result);
  
  //
  // Done
  //
  
  exit(0);
  
}
